function shouldSkipString(inner) {
    if (!inner || inner.length === 0) return true;
    if (inner.startsWith('assets/') || inner.includes('assets/fitness')) return true;
    if (/^#[\w-]+$/.test(inner)) return true;
    if (/^m[1-7]$/.test(inner)) return true;
    // skip lowercase ids/paths only — allow Foresight, Gate, Scan, etc. in prose
    if (/^[a-z][a-z0-9_-]*$/.test(inner) && inner.length < 40 && !/[\u0600-\u06FF]/.test(inner)) return true;
    if (/^var\(--/.test(inner)) return true;
    if (/^https?:\/\//.test(inner)) return true;
    if (/\.(svg|png|jpg|js|css|html)$/.test(inner)) return true;
    if (/^foresight_concept\.png$/.test(inner)) return true;
    return false;
}

function persianizeInner(inner) {
    let s = inner;
    for (const [from, to] of REPS) {
        if (from === to) continue;
        s = s.split(from).join(to);
    }
    // فرود/صعود narrative labels → natural Persian
    s = s.replace(/"فرود: ([^"]+)"/g, '"$1"');
    s = s.replace(/"صعود: ([^"]+)"/g, '"$1"');
    s = s.replace(/'فرود: ([^']+)'/g, "'$1'");
    s = s.replace(/'صعود: ([^']+)'/g, "'$1'");
    s = s.replace(/\. فرود: /g, '. در عمل، ');
    s = s.replace(/\. صعود: /g, '. راه دیگر: ');
    s = s.replace(/فرود: /g, '');
    s = s.replace(/صعود: /g, 'در مقابل، ');
    s = s.replace(/§۱–§۶ · فرود و صعود هر بعد/, '§۱–§۶ · پنج بعد با مثال عملی');
    // slide titles: drop narrative prefix
    if (s.startsWith('در مقابل، ') && s.length < 90 && !s.includes('<')) {
        s = s.replace(/^در مقابل، /, '');
    }
    s = s.replace(/Corporate آینده‌نگاری/g, 'Corporate Foresight');
    s = s.replace(/  +/g, ' ');
    s = s.replace(/، ،/g, '،');
    s = s.replace(/  ·/g, ' ·');
    return s;
}

function processFile(rel) {
    const p = path.join(process.cwd(), rel);
    const src = fs.readFileSync(p, 'utf8');
    const out = src.replace(/'((?:\\.|[^'\\])*)'/g, (m, inner) => {
        if (shouldSkipString(inner)) return m;
        const next = persianizeInner(inner);
        return next === inner ? m : `'${next}'`;
    });
    fs.writeFileSync(p, out);
    console.log('Processed', rel);
}

for (const f of FILES) processFile(f);
